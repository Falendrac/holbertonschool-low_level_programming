#include "3-calc.h"

/**
 * op_add - Returns the sum of a and b
 *
 * @a: The first arg we add
 * @b: The second arg we add
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Returns the difference of a and b
 *
 * @a: The first arg we sub
 * @b: The second arg we sub
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Returns the product of a and b
 *
 * @a: The first arg
 * @b: The second arg
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Returns the division of a and b
 *
 * @a: The first arg
 * @b: The second arg
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Returns the remainder of a and b
 *
 * @a: The first arg
 * @b: The second arg
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
